                <h2 class="article-title">司马懿城府那么深，为何偏偏惧怕曹叡？</h2>
                <div class="article-sub"><!----> <span>书绣香</span> <span>2018-01-09 17:16:40</span></div>
                <div class="article-content"><div><p>三国时期，除了诸葛亮意外，最令人敬佩的也就是司马懿了，因为这个人物也是一个比较厉害的角色，不仅有才能，还有实力。可能他的能力跟曹家不相上下，但是不知为何一直没有实施篡权，那么这究竟是因为什么呢？司马懿又为什么害怕曹睿？</p><p>曹睿是曹丕的长子，也是继曹丕之后，曹魏的第二位皇帝。他在位的十三年间，前期颇有建树，后期沉迷于享乐，后来又托孤不当，造成了后期曹魏政局的动荡。曹睿字元仲，睿的意思是明智和智慧，也就是通达的意思。而元则有第一、首位的意思，仲则是排在第二的意思。</p><p>所以，字“元仲”与本名“睿”表达的大意相同，都是希望曹睿能够出类拔萃。但是，曹睿的字也引起了一些争议，而这些争议，往往与他的身世有关。因为曹睿的字的谐音是“袁种”，也有人怀疑其中暗含着另一层意思，那就是曹睿其实是其生母甄宓的前夫袁熙的儿子。</p><p><img src="http://p1.pstatp.com/large/568b000156f5694166a4" img_width="870" img_height="511" alt="司马懿城府那么深，为何偏偏惧怕曹叡？" inline="0"></p><p>至于到底有没有这层暗含的意思，其实就要看这是谁给曹睿起的字。如果是曹丕所起的话，那么应该就没有这层暗含的意思。毕竟这对男人来说是奇耻大辱，他不可能再取这样的一个谐音来供人猜测，来时刻提醒着自己曹睿不是自己的儿子。</p><p>如果这是甄宓所起的话，那就另当别论了。毕竟，甄宓与袁熙情投意合，彼此感情深厚，通过这样的谐音来给他们俩的儿子取字，也是对袁熙的一种纪念。当然，谁给曹睿起的字，就如同曹睿的身世之谜一样，如今已是无从追溯了。</p><p>曹睿是曹魏的皇帝，他的后宫中有无数的女人等着他去临幸。但是不知为何，曹睿的子嗣非常单薄，他一共有三个亲生的儿子，两个养子以及两个女儿。可惜的是，他的帝位，最后也没能传到自己亲生儿子的手中。</p><p><img src="http://p3.pstatp.com/large/568b000156f4653ac099" img_width="482" img_height="360" alt="司马懿城府那么深，为何偏偏惧怕曹叡？" inline="0"></p><p>司马懿是三国历史上让人颇为忌惮的人物，他是曹魏的大军师，他辅佐曹丕开创了大魏，他大权在握，他一生在活在权谋与算计当中，鹰视狼顾是世人对于司马懿的评价。但是出人意料的是，司马懿一生都没有对曹魏产生二心，没有因为势力的强大而篡位谋反，就连曹丕死后司马懿仍然尽心尽力的辅佐曹睿。</p><p>曹睿的才智不输给曹丕，而且曹氏宗族势力也很庞大，关键在于，司马懿打仗给他兵权，打完就收走。司马懿压根就没办法顽抗。曹睿是很有能力的君主，如果不是英年早逝，司马家未必能够篡位。</p><p>曹睿是不信任司马懿的，无论是家族个性的遗传，祖父曹操的教诲，仍是关于当时权力构架的组织，终究托孤时为司马懿组织了曹爽这个死对头，都表现出曹睿关于司马懿的不放心，但是曹睿想降服司马懿的心，让他为曹魏统一天下耗尽最后一滴汗水。司马懿既要体现出自个的价值，又不能让曹睿这个大老板心生猜疑，上天给他组织了两个贵人：诸葛亮、陆逊。</p><p><img src="http://p3.pstatp.com/large/568b000156f308480a77" img_width="435" img_height="253" alt="司马懿城府那么深，为何偏偏惧怕曹叡？" inline="0"></p><p>为了能持续生计下去，司马懿还活跃投合曹睿的喜好，当诸葛亮身后，曹睿认为最大的威胁现已不见，便大举吃苦，大修宫室，魏国大都大臣劝阻曹睿要勤政爱民，这儿却没有司马懿的声响，他采纳缄默沉静支撑的态度，曹睿关于他放下点了戒心。</p><p>不过人算不如天算，魏明帝曹睿年仅三十六岁，就因病医治无效去世了。在临死前，聪明一世的曹睿，却犯下了一个使曹家遭遇灭顶之灾的决定：立年仅八岁的曹芳为帝，由司马懿和大将军曹爽共同辅政。</p><p>司马懿的野心，也就是从曹睿去世之后，逐渐开始膨胀。此时，他手中才真正的掌握了大权，没有人可以将其分分钟收回。唯一能与之相抗衡的势力就是曹爽。而这个曹爽又是个大草包。几年之后，司马懿发动高平陵政变，以曹爽谋反的名义，将其诛杀。此后，司马家族就独揽了魏国的军政大权。</p></div></div>
